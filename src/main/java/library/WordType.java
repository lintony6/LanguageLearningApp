package library;

/**  
This can be used to classify words based on their grammatical roles, such as
nouns, verbs, adjectives, and adverbs.
*/
public enum WordType {

    /**
Represents a noun, which is a word used to identify people, places, things, or ideas.
    */
  NOUN,

    /**    
Represents a verb, which is a word used to describe actions, occurrences, or states.*/
  VERB,

    /**
Represents an adjective, which is a word that modifies or describes a noun or pronoun.
    */
  ADJECTIVE,

    /**   
Represents an adverb, which is a word that modifies or describes a verb, an adjective, or another adverb.
    */
  ADVERB;
}